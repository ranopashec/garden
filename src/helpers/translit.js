/**
 * Транслитерация кириллицы в латиницу
 * @param {string} str - Строка для транслитерации
 * @returns {string} - Транслитерированная строка
 */
function transliterate(str) {
  if (!str) return '';
  
  const translitMap = {
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
    'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
    'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
    'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch',
    'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
    'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo',
    'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
    'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
    'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sch',
    'Ъ': '', 'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya'
  };
  
  return str
    .split('')
    .map(char => translitMap[char] || char)
    .join('')
    .toLowerCase() // Все в нижний регистр для URL
    .replace(/[^a-z0-9]+/g, '-') // Заменяем не-латинские символы на дефисы
    .replace(/^-+|-+$/g, ''); // Убираем дефисы в начале и конце
}

/**
 * Обратная транслитерация (латиница -> кириллица)
 * Это упрощённая версия, работает только для базовых случаев
 * @param {string} str - Строка для обратной транслитерации
 * @returns {string} - Строка на кириллице
 */
function reverseTransliterate(str) {
  if (!str) return '';
  
  // Это сложная задача, так как транслитерация неоднозначна
  // Для простоты вернём исходную строку, если она уже на кириллице
  // Или попробуем найти соответствие в базе известных переводов
  // В реальности лучше хранить маппинг транслит -> оригинал
  
  // Пока просто возвращаем как есть, если нужно будет - можно улучшить
  return str;
}

module.exports = {
  transliterate,
  reverseTransliterate
};

